public class DataTypes {
    public static void main(String[] args) {
        int a=10;
        byte b=9;
        short c=23;
        long d = 45l;

        boolean  e=false;
        char f='k';
        float g=10.2f;
        double h=78.36;
        System.out.println(a+" "+b+" "+c+" "+d+" "+e+" "+f+" "+g+" "+h);

        String name="Fani";
        int age=22;
        char gender='M';
        double salary=0000000.0;
        System.out.println(name+age+gender+ salary);

        // typecasting
        int i=20;
        long l=i;
        System.out.println(l);

        long ll=25;
        int aa=(int) ll;
        System.out.println(aa);

        int a1=90;
        float ff= a1;
        System.out.println(ff);
        int a2= (int) ff;
        System.out.println(a2);


    }
}
